# Arithmetic operators

# + -> addition or concatenation
print('5+3=',5+3) #addition
print('5+3=','Hiii'+'Everyone') #concatenation
# - -> subtraction
print('5-3=',5-3)
# * -> multiplication
print('5*3=',5*3)
# / -> division
print(f"15 / 2 = {15/2}")
# // -> modulus
print(f"15 // 2 = {15 // 2}")
# % -> remainder
print(f"15 % 2 = {15%2}")
# ** -> power
print(f"2**5={2**5}") #2*2*2*2*2